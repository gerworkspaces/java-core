package VongLap;

public class VongLapWhile {
    public static void main(String[] args) {
        int i = 0;
        while (i < 5){
            System.out.println(i);
            i += 1;
            System.out.println("-----");
        }

        int[] l = {1,2,3,4,5};
        int index = 0;
        while (index < l.length){
            System.out.println(l[index]);
            index++;
            System.out.println("-----");

        }

    }
}
